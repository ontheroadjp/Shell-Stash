<template><h1 id="shell-stash" tabindex="-1"><a class="header-anchor" href="#shell-stash" aria-hidden="true">#</a> Shell Stash</h1>
<p>このスクリプトは、シェルに一時的なファイルやディレクトリの隠し場所を提供します。
ファイルやディレクトリは、どのディレクトリからでもこのスタッシュに出し入れすることができます。</p>
<h2 id="何が便利なのか" tabindex="-1"><a class="header-anchor" href="#何が便利なのか" aria-hidden="true">#</a> 何が便利なのか？</h2>
<p>Shell-Stashをインストールすれば、長いパスを指定してmvやcpコマンドを使う必要はありません。</p>
<p>シェルで <code>mv</code> や <code>cp</code> コマンドを使用している場合は、それらを使用する代わりに、単に stash に入れてディレクトリを移動し、その後すぐに stash から削除することができます。</p>
<p>しかし、ディレクトリを移動するたびに <code>cd</code> コマンドを使っていたのでは、Shell-Stashの効果は思ったほどではないかもしれません。</p>
<p>そのような場合は、まずディレクトリ移動のための環境を整えておくとよいでしょう。</p>
</template>
